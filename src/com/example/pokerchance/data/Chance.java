package com.example.pokerchance.data;

public class Chance {
	
	private int outs;
	private float ternChance;
	private float riverChance;
	private float ternAndRiverChance;
	
	private float ternPercent;
	private float riverPercent;
	private float ternRiverPercent;
	
	public int getOuts() {
		return outs;
	}
	public void setOuts(int outs) {
		this.outs = outs;
	}
	public float getTernChance() {
		return ternChance;
	}
	public void setTernChance(float ternChance) {
		this.ternChance = ternChance;
	}
	public float getRiverChance() {
		return riverChance;
	}
	public void setRiverChance(float riverChance) {
		this.riverChance = riverChance;
	}
	public float getTernAndRiverChance() {
		return ternAndRiverChance;
	}
	public void setTernAndRiverChance(float ternAndRiverChance) {
		this.ternAndRiverChance = ternAndRiverChance;
	}
	public float getTernPercent() {
		return ternPercent;
	}
	public void setTernPercent(float ternPercent) {
		this.ternPercent = ternPercent;
	}
	public float getRiverPercent() {
		return riverPercent;
	}
	public void setRiverPercent(float riverPercent) {
		this.riverPercent = riverPercent;
	}
	public float getTernRiverPercent() {
		return ternRiverPercent;
	}
	public void setTernRiverPercent(float ternRiverPercent) {
		this.ternRiverPercent = ternRiverPercent;
	}
	
	
	
	
}
